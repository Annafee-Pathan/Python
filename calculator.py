def add(x,y):
    return x+y
def subtract(x,y):
    return x-y
def multiply(x,y):
    return x*y
def divide(x,y):
    return x/y

n1=int(input("Enter Number 1 :"))
n2=int(input("Enter Number 2 :"))

print("Sum:",add(n1,n2))
print("Difference:",subtract(n1,n2))
print("Product:",multiply(n1,n2))
print("Quotient:",divide(n1,n2))
